public class Var {
    public static void main(String[] args) {

        // assign binary number to var-variable
        var binary = 0b10101010;

        // assign hexadecimal number to var-variable
        var hexadecimal = 0xAA00;

        // Print converted number to console
        System.out.println("Binary: 10101010 -> Decimal: " + binary);
        System.out.println("Hexadecimal: 0xAA00 -> Decimal: "+ hexadecimal);
    }
}