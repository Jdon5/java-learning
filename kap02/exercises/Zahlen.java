public class Zahlen {
    public static void main(String[] args) {

        // assigned binary number to an int-variable
        int binary = 0b10101010;

        // assigned hexadecimal number to an int variable
        int hexa = 0xAA00;

        // print converted values to the console
        System.out.println("Binary: 10101010 -> Decimal: " + binary);
        System.out.println("Hexadecimal: 0xAA00 -> Decimal: "+ hexa);
    }
}